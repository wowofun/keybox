enum Language: String, CaseIterable, Identifiable {
    case english = "en"
    case chineseSimplified = "zh-Hans"
    case chineseTraditional = "zh-Hant"
    case japanese = "ja"
    case korean = "ko"
    case french = "fr"
    case german = "de"
    case spanish = "es"
    case italian = "it"
    case portuguese = "pt"
    case russian = "ru"
    case arabic = "ar"
    case hindi = "hi"
    case thai = "th"
    case vietnamese = "vi"
    case indonesian = "id"
    
    var id: String { rawValue }
    
    var displayName: String {
        switch self {
        case .english: return "English"
        case .chineseSimplified: return "简体中文"
        case .chineseTraditional: return "繁體中文"
        case .japanese: return "日本語"
        case .korean: return "한국어"
        case .french: return "Français"
        case .german: return "Deutsch"
        case .spanish: return "Español"
        case .italian: return "Italiano"
        case .portuguese: return "Português"
        case .russian: return "Русский"
        case .arabic: return "العربية"
        case .hindi: return "हिन्दी"
        case .thai: return "ไทย"
        case .vietnamese: return "Tiếng Việt"
        case .indonesian: return "Bahasa Indonesia"
        }
    }
}

class LocalizationManager: ObservableObject {
    static let shared = LocalizationManager()
    
    @Published var language: Language {
        didSet {
            UserDefaults.standard.set(language.rawValue, forKey: "selectedLanguage")
        }
    }
    
    private var translations: [String: [String: String]] = [:]
    
    init() {
        if let savedLang = UserDefaults.standard.string(forKey: "selectedLanguage"),
           let lang = Language(rawValue: savedLang) {
            self.language = lang
        } else {
            self.language = .english
        }
        loadTranslations()
    }
    
    func localized(_ key: String) -> String {
        guard let langDict = translations[language.rawValue],
              let value = langDict[key] else {
            // Fallback to English
            if let enDict = translations["en"], let enValue = enDict[key] {
                return enValue
            }
            return key
        }
        return value
    }
    
